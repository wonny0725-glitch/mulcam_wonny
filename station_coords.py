def get_station_coord(station_name: str) -> dict:
    """
    역명으로 위경도 좌표 조회
    Args:
        station_name: 역명 (예: "강남")
    Returns:
        {"lat": 위도, "lng": 경도, "line": "호선"} 또는 None
    """
    return STATION_COORDS.get(station_name)


def get_all_stations() -> list:
    """
    모든 역명 리스트 반환
    Returns:
        역명 리스트
    """
    return list(STATION_COORDS.keys())


def get_stations_by_line(line: str) -> list:
    """
    특정 호선의 역 리스트 반환
    Args:
        line: 호선명 (예: "1호선")
    Returns:
        해당 호선의 역명 리스트
    """
    return [name for name, coord in STATION_COORDS.items() if coord["line"] == line]

